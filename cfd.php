			ICCS 2019 will again feature a special demonstration session showcasing the latest technological advances in the area of consumer communications and networking. In addition, there will be a unique opportunity for a small number of demonstrators to be shown to the greater public (~150,000 visitors) at the International CES 2019 in the Las Vegas Convention Center (LVCC), for which we have organized space at a booth.<br>

				The demonstration session can host up to 30 demonstrators and will provide an exciting forum for researchers, developers, and entrepreneurs to present and discuss new applications and techniques, research demonstrations, recent research/implementation results, upcoming research challenges, practical implementations, commercial developments, future directions and novel application concepts. We envision the demonstration session as a means to exhibit innovations that have the potential to change the future landscape of consumer communications and networking.	<br>

				We invite researchers and developers from academic, industrial, and government laboratories to present their novel ideas and demonstrations of original research prototypes. ICCS especially welcomes demonstrations of emerging and innovative technologies that have potential commercial application from nascent and incubating companies.  In addition, authors that have a demonstration component in their work and are planning to submit their papers to the main tracks are highly encouraged to also submit proposals to the demonstration session. <br>
				Each tutorial proposal must contain the following:<br><br>

				
				<b>PROPOSAL FORMAT:</b>	<br>
				Demonstration proposals should be one or two pages in length, in double-column IEEE format, including authors' names and affiliations and a short abstract.  Each proposal should describe the main contributions of the demonstration and the merits of the proposed idea. Of course, many variations are possible. A technology prototype can range from a mockup using computers (instead of actual consumer devices) to a full-fledged collection of networked consumer devices. Be as specific as possible in describing what you will show. In addition to the proposal to be published, an additional e-mail should be sent to the chairs, including space, power and networking requirements.<br>

				Demonstration proposals will be evaluated by a Committee including representatives of venture capitalists. Proposals will be evaluated mainly based on their potential to stimulate interesting discussions, exchange of ideas, and promote collaborations. The accepted demonstration proposals will also appear in the conference proceedings as well as on IEEE Xplore. The best demonstrator will receive a Demo Award.<br><br>
								<b>PRESENTATIONS:</b><br>
				Accepted demonstrations will be presented at the conference during a special demonstration session. The presenter is responsible for the demonstration and equipment setup. Each demonstration should be accompanied by a poster of approximately 30 inches by 40 inches describing it. The presenter is expected present and discuss the work with other conference attendees. For every demonstrator, a large table, a power socket, and Wi-Fi Internet connectivity will be made available.  Please consult the Demonstrations Co-Chairs if you are uncertain whether your demonstration falls within the scope of the conference or if you require additional information.<br><br>

				<b>INTERNATIONAL CES 2019</b><br>
				IEEE ICCS has reserved a space at the 2019 International CES (January 8 – 11) in Las Vegas Convention Center (LVCC). Presence in International CES will be a unique opportunity for a small number of demonstrations to be shown to the greater public. These demonstrations will be selected by the Demonstration Committee from the relevant proposals submitted for the IEEE ICCS Demonstrations Session. The following apply:<br><br>

				<i class="fa fa-circle"></i>&nbsp Authors who would like their demonstration to be considered for presentation at International CES 2019 should send an e-mail to the Demonstration Co-Chairs by September 30 2018. &nbsp&nbsp&nbsp&nbsp&nbspPresentation at International CES is conditioned by the final acceptance of their proposal.<br>
				<i class="fa fa-circle"></i>&nbspThe proposed demonstration should have the potential to represent the leading edge spirit of the conference.<br>
				<i class="fa fa-circle"></i>&nbspThe proposer should be willing to present his/her demonstration at IEEE ICCS as well as during selected days of the International CES.<br>
				<i class="fa fa-circle"></i>&nbspThe proposing organization or company should become an official patron of IEEE ICCS 2019 to help cover the costs of the booth. View the Patronage packages.<br>
				&nbsp&nbsp&nbsp&nbsp&nbspAll proposals and questions should be submitted to the Demonstrations Co-chairs.<br><br>

