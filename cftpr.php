			The CCNC 2019 organizing committee invites proposals for tutorials, which are half-day (3 hours) or full-day (6 hours) in length and will take place on the first and last day of the conference (January 11 and 14).  The tutorials will be open to all registered conference attendees without an additional charge.  We seek proposals across a wide range of topics and levels — ranging from fundamentals to the latest advances in hot topic areas. Topics of interest include all areas of consumer communications and networking.<br><br>
			
			<b>PROPOSAL FORMAT:</b><br>
				Each tutorial proposal must contain the following:<br>

				  1.&nbspTitle<br>
				2.&nbspNames and Affiliations of Speakers<br>
				 3.&nbspAbstract (one or two paragraphs)<br>
				4.&nbspIntended Audience (one paragraph): Describe the background assumed of tutorial attendees. Tutorials may range from basic for attendees new to the field   to advanced topics for experts.<br>
				 5.&nbspDescription: A statement (no more than 3 pages) giving clear motivation/justification for the topic to be presented at CCNC and a comprehensive outline   of the proposed content.<br> 
				 6.&nbspMaterials: A description (one or two paragraphs) of materials to be provided to attendees on the conference website – course slides, annotated   bibliography, code snippets, etc. NOTE:<br> &nbsp &nbsp &nbsp the materials themselves do not need to be provided in the proposal.<br>
				7.&nbspBio-sketches: A half-page bio-sketch of each tutorial presenter.<br>
				<b>SELECTION:</b><br>
				All tutorial proposals will first undergo peer review. Then, from the pool of proposals deemed of the highest quality, the chairs will make the final selection of tutorials to present the greatest variety to conference attendees and to best fit the conference schedule.<br>

				<b>UPON ACCEPTANCE:</b><br>
				Speakers for all accepted tutorials will be required to sign a commitment to present at the conference within 14 days of notification. Tutorial topics and their presenters will be featured on the CCNC 2018 website before the paper submission deadline.  Materials for the tutorial must be emailed at least 14 days before the presentation date.<br>
				 
